                        <b>De zwarte dood in Europa</b><br>
                        
                        In de 14e eeuw, tijdens de Hanzeperiode, heerste er een allesverwoestende ziekte in heel Europa.
                        Deze ziekte werd de zwarte dood genoemd, en ging rond tussen de jaren 1346 en 1351. In deze korte periode zijn er toen tot 50 miljoen mensen aan overleden.
                        In Europa leefden er in die tijd ook al behoorlijk veel mensen! <br>
                        Maar in tegenstelling tot nu, hadden mensen toen geen riolering of vuilnismannen. 
                        Men wisten toen nog niet goed dat hygiëne (zoals handen wassen, douchen en niet je poep op straat gooien!) belangrijk was voor een goede gezondheid. 
                        Het was dus een drukke en vieze boel. Op deze manier konden er makkelijk ziektes ontstaan. 
                        Omdat mensen zoveel in aanraking kwamen met elkaar, dieren en afval, zonder daarna te wassen, was het heel makkelijk om elkaar te besmetten. <br>
                        <br>
                        Waarschijnlijk is de Zwarte Dood via de handel en scheepvaart naar Europa gekomen. 
                        Eenmaal in Italië deed de ziekte zijn verschrikkelijke werk. Veel mensen vluchtten weg voor de ziekte. 
                        De bacterie ging mee met die mensen en zo kwam de ziekte ook in andere landen in Europa.
                        <br>
                        Veel mensen dachten dat het einde van de wereld was aangebroken of dat dit een straf van God was.. Midden op straat zakten er soms mensen in elkaar 
                        en niemand verpleegde hen. De pestdokters met hun rare vogelachtige maskers liepen rond zonder echt te kunnen behandelen. <br>
                        <br>
                        Wat in die periode de zwarte dood werd genoemd, was eigenlijk een vorm van de <i>pest</i>.
                        De pest is een infectieziekte die mensen kregen door een beet van een zieke vlo. Deze vlo was besmet met een bacterie. Deze vlo zat het liefste op de zwarte rat. 
                        En er waren veel zwarte ratten in de middeleeuwen! De vlo sprong over naar de mens en beet haar ook. En zo raakten de mensen ook besmet.<br>
                        <br>
                        De Zwarte Dood had een vreselijke nasleep. Omdat zoveel mensen overleden waren, waren er ook geen mensen om het land te onderhouden. 
                        De oogsten mislukte en hongersnood sloeg toe. De steden waren opeens veel leger. 
                        Nog regelmatig zou de pest soms opduiken in Europa, maar nooit zo erg als in 1348.
                        
